package com.cz.wisdomcity.entity;

public class LoginInfo {

	public String name;
	public String password;
	public String email;
	public int type;

	public LoginInfo() {
	}

	public LoginInfo(String name, String password, String email, int type) {
		this.name = name;
		this.password = password;
		this.email = email;
		this.type = type;

	}
}
